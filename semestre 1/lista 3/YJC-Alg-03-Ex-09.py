dia = input("Digite a data de um dia (Ex:28 de julho):").lower()

if dia == "1 de janeiro":
    print("Confraternização universal")
elif dia == "21 de abril":
    print("Tiradentes")
elif dia == "1 de maio":
    print("Dia do trabalho")
elif dia == "7 de setembro":
    print("Indepêndencia do Brasil")
elif dia == "12 de outubro":
    print("Nossa Senhora Aparecida")
elif dia == "2 de novembro":
    print("Finados")
elif dia == "15 de novembro":
    print("Proclamação da república")
elif dia == "25 de dezembro":
    print("Natal")
else:
    print("Não é feriado nacional")